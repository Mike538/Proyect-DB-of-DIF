CREATE TABLE PACIENTE (
    CLAVE_PACIENTE NUMBER PRIMARY KEY,
    NOMBRE VARCHAR2(50),
    APELLIDO_PATERNO VARCHAR2(50),
    APELLIDO_MATERNO VARCHAR2(50),
    NO_TELEFONICO NUMBER,
    DIRECCION VARCHAR2(70),
    GENERO VARCHAR2(50)
);

CREATE TABLE PSICOLOGO (
    CLAVE_PSICOLOGO NUMBER PRIMARY KEY,
    NOMBRE VARCHAR2(50),
    APELLIDO_PATERNO VARCHAR2(50),
    APELLIDO_MATERNO VARCHAR2(50),
    ESPECIALIZACION VARCHAR2(50),
    NUMERO_CONTACTO NUMBER,
    JORNADA DATE
);

CREATE TABLE GRUPO_EDAD (
    NOMBRE_GRUPO VARCHAR2(50) PRIMARY KEY,
    EDAD_MINIMA NUMBER,
    EDAD_MAXIMA NUMBER
);

CREATE TABLE ESTADO_CITAS (
    CLAVE_ESTADO_CITA NUMBER PRIMARY KEY,
    ESTADO VARCHAR2(50),
    TOLERANCIA_MINUTOS NUMBER
);

CREATE TABLE CITA (
    CLAVE_CITA NUMBER PRIMARY KEY,
    HORA DATE,
    FECHA DATE,
    MOTIVO_CITA VARCHAR2(70),
    CLAVE_CIE11 NUMBER,
    NUMERO_EXPEDIENTE VARCHAR2(50),
    CLAVE_PACIENTE NUMBER,
    CLAVE_PSICOLOGO NUMBER,
    CLAVE_ESTADO_CITA NUMBER,
    NOMBRE_GRUPO VARCHAR2(50),
    FOREIGN KEY (CLAVE_PACIENTE) REFERENCES PACIENTE(CLAVE_PACIENTE),
    FOREIGN KEY (CLAVE_PSICOLOGO) REFERENCES PSICOLOGO(CLAVE_PSICOLOGO),
    FOREIGN KEY (CLAVE_ESTADO_CITA) REFERENCES ESTADO_CITAS(CLAVE_ESTADO_CITA),
    FOREIGN KEY (NOMBRE_GRUPO) REFERENCES GRUPO_EDAD(NOMBRE_GRUPO)
);
